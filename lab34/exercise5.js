let day = "Tuesday";

switch(day) {
    case "Monday":
        console.log("Start of the week!");
        break;
    case "Friday":
        console.log("Tomorrow is the weekend!");
        break;
    case "Sunday":
        console.log("It's a rest day.");
        break;
    default:
        console.log("It's a regular day.");
        break;
}

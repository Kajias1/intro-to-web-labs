import math

# Built-in "min" and "max" functions
x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

# Built-in "abs" function
x = abs(-7.25)

print(x)

# Built-in "pow" function
x = pow(4, 3)

print(x)

# Using "math" module
x = math.sqrt(64)

print(x)

x = math.ceil(1.4)
y = math.floor(1.4)

print(x) # returns 2
print(y) # returns 1

# PI constant
x = math.pi

print(x)